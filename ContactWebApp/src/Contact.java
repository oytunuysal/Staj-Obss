
public class Contact {
	public String name;
	public String gsm;

	public Contact(String name, String gsm) {
		this.name = name;
		this.gsm = gsm;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getGsm() {
		return gsm;
	}

	public void setGsm(String gsm) {
		this.gsm = gsm;
	}

}
